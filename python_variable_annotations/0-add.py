#!/usr/bin/env python3
"""
This is a module for performing basic addition arithmetic operations.

Task 0. Basic annotations - add

Write a type-annotated function add that takes a float a and a float b as
arguments and returns their sum as a float
"""


def add(a: float, b: float) -> float:
    """
    Add two floating-point numbers and return the result.

    :param a: First floating-point number
    :param b: Second floating-point number
    :return: Sum of a and b
    """
    return a + b
